import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

async function exists(p) {
  try {
    await fs.lstat(p);
    return true;
  } catch {
    return false;
  }
}

async function main() {
  const repoRoot = path.resolve(__dirname, '..', '..');
  const jsonNodeModules = path.join(repoRoot, 'json', 'node_modules');
  const rootNodeModules = path.join(repoRoot, 'node_modules');

  if (!(await exists(jsonNodeModules))) return;
  if (await exists(rootNodeModules)) return;

  // On Windows, creating a symlink often requires admin unless using a junction.
  // Node supports 'junction' for directory links on Windows.
  await fs.symlink(jsonNodeModules, rootNodeModules, 'junction');
}

main().catch((err) => {
  // eslint-disable-next-line no-console
  console.error(err);
  process.exitCode = 1;
});
